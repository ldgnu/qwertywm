// Command gen generates Go bindings for Wayland protocols from their XML
// descriptions. It is invoked via go:generate (see gen.go at the repository
// root).
//
// Usage:
//
//	gen -package <name> -out <file.go> <protocol.xml>...
//
// All interfaces from all listed protocol files are emitted into a single
// package. Object and enum references are resolved within that package;
// references to interfaces outside it (e.g. a river protocol referring to
// wl_surface) degrade to the untyped wire.Object interface.
//
// wl_display, wl_registry, and wl_callback are skipped: they are hand
// written in the wire package because they bootstrap the object system.
package main

import (
	"bytes"
	"encoding/xml"
	"flag"
	"fmt"
	"go/format"
	"os"
	"strings"
)

// ---------------------------------------------------------------------------
// Protocol XML schema
// ---------------------------------------------------------------------------

type Protocol struct {
	Name       string      `xml:"name,attr"`
	Interfaces []Interface `xml:"interface"`
}

type Interface struct {
	Name     string    `xml:"name,attr"`
	Version  int       `xml:"version,attr"`
	Requests []Message `xml:"request"`
	Events   []Message `xml:"event"`
	Enums    []Enum    `xml:"enum"`
}

type Message struct {
	Name        string `xml:"name,attr"`
	Type        string `xml:"type,attr"` // "destructor" or ""
	Since       int    `xml:"since,attr"`
	Args        []Arg  `xml:"arg"`
	Description Desc   `xml:"description"`
}

type Arg struct {
	Name      string `xml:"name,attr"`
	Type      string `xml:"type,attr"`
	Interface string `xml:"interface,attr"`
	Enum      string `xml:"enum,attr"`
	AllowNull bool   `xml:"allow-null,attr"`
	Summary   string `xml:"summary,attr"`
}

type Enum struct {
	Name     string  `xml:"name,attr"`
	Bitfield bool    `xml:"bitfield,attr"`
	Entries  []Entry `xml:"entry"`
}

type Entry struct {
	Name    string `xml:"name,attr"`
	Value   string `xml:"value,attr"`
	Summary string `xml:"summary,attr"`
}

type Desc struct {
	Summary string `xml:"summary,attr"`
	Body    string `xml:",chardata"`
}

// handwritten interfaces live in the wire package.
var handwritten = map[string]bool{
	"wl_display":  true,
	"wl_registry": true,
	"wl_callback": true,
}

// wireTypes maps handwritten interfaces to their Go types in the wire
// package, for when another interface references them in an argument
// (e.g. wl_surface.frame returns a new wl_callback).
var wireTypes = map[string]string{
	"wl_callback": "wire.Callback",
}

func main() {
	pkg := flag.String("package", "", "output package name")
	out := flag.String("out", "", "output file")
	flag.Parse()
	if *pkg == "" || *out == "" || flag.NArg() == 0 {
		fmt.Fprintln(os.Stderr, "usage: gen -package <name> -out <file.go> <protocol.xml>...")
		os.Exit(2)
	}

	g := &generator{pkg: *pkg, known: make(map[string]bool)}
	var protos []Protocol
	for _, path := range flag.Args() {
		data, err := os.ReadFile(path)
		if err != nil {
			fatal(err)
		}
		var p Protocol
		if err := xml.Unmarshal(data, &p); err != nil {
			fatal(fmt.Errorf("%s: %w", path, err))
		}
		protos = append(protos, p)
		for _, iface := range p.Interfaces {
			if !handwritten[iface.Name] {
				g.known[iface.Name] = true
			}
		}
	}

	g.emitHeader(flag.Args())
	for _, p := range protos {
		for _, iface := range p.Interfaces {
			if handwritten[iface.Name] {
				continue
			}
			g.emitInterface(iface)
		}
	}

	src, err := format.Source(g.buf.Bytes())
	if err != nil {
		// Write the unformatted source anyway so the error is debuggable.
		os.WriteFile(*out, g.buf.Bytes(), 0o644)
		fatal(fmt.Errorf("generated code does not compile (wrote unformatted output to %s): %w", *out, err))
	}
	if err := os.WriteFile(*out, src, 0o644); err != nil {
		fatal(err)
	}
}

func fatal(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}

// ---------------------------------------------------------------------------
// Code generation
// ---------------------------------------------------------------------------

type generator struct {
	pkg   string
	buf   bytes.Buffer
	known map[string]bool // interfaces defined in this package
}

func (g *generator) p(format string, args ...any) {
	fmt.Fprintf(&g.buf, format+"\n", args...)
}

func (g *generator) emitHeader(sources []string) {
	g.p("// Code generated by internal/gen from %s. DO NOT EDIT.", strings.Join(sources, " "))
	g.p("")
	g.p("package %s", g.pkg)
	g.p("")
	g.p("import (")
	g.p("\t\"fmt\"")
	g.p("")
	g.p("\t\"qwertywm/wire\"")
	g.p(")")
	g.p("")
	g.p("// silence unused-import errors for protocols that never use fmt")
	g.p("var _ = fmt.Errorf")
}

// goName converts a wayland interface name to a Go type name within the
// package: wl_output -> Output, river_window_v1 -> WindowV1.
func goName(iface string) string {
	s := iface
	for _, prefix := range []string{"wl_", "river_", "zwp_", "zwlr_", "ext_", "wp_", "xdg_"} {
		if strings.HasPrefix(s, prefix) && len(s) > len(prefix) {
			s = s[len(prefix):]
			break
		}
	}
	return camel(s)
}

// camel converts snake_case to CamelCase.
func camel(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "")
}

// lowerCamel converts snake_case to camelCase, avoiding Go keywords.
func lowerCamel(s string) string {
	c := camel(s)
	c = strings.ToLower(c[:1]) + c[1:]
	switch c {
	case "type", "interface", "map", "range", "func", "var", "chan", "select":
		return c + "_"
	}
	return c
}

func (g *generator) emitInterface(iface Interface) {
	tn := goName(iface.Name)
	g.p("")
	g.p("// ---------------------------------------------------------------------------")
	g.p("// %s", iface.Name)
	g.p("// ---------------------------------------------------------------------------")
	g.p("")
	g.p("// %sName is the Wayland interface name of %s.", tn, tn)
	g.p("const %sName = %q", tn, iface.Name)
	g.p("")
	g.p("// %sVersion is the highest version of %s supported by these bindings.", tn, tn)
	g.p("const %sVersion = %d", tn, iface.Version)

	// Enums.
	for _, e := range iface.Enums {
		g.emitEnum(tn, e)
	}

	// Struct with event handler fields.
	g.p("")
	g.p("// %s is a proxy for the %s interface.", tn, iface.Name)
	g.p("type %s struct {", tn)
	g.p("\twire.Proxy")
	for _, ev := range iface.Events {
		g.p("")
		if ev.Description.Summary != "" {
			g.p("\t// On%s: %s", camel(ev.Name), ev.Description.Summary)
		} else {
			g.p("\t// On%s handles the %s event.", camel(ev.Name), ev.Name)
		}
		g.p("\tOn%s func(%s)", camel(ev.Name), g.eventParams(iface, ev))
	}
	g.p("}")
	g.p("")
	g.p("// Interface returns the Wayland interface name %q.", iface.Name)
	g.p("func (*%s) Interface() string { return %q }", tn, iface.Name)

	// Bind helper for globals: any interface can in principle be bound from
	// the registry; emit a helper for all of them since it costs nothing.
	g.p("")
	g.p("// Bind%s binds the global named name on the registry to a new %s.", tn, tn)
	g.p("// The version must be <= the advertised version and <= %sVersion.", tn)
	g.p("func Bind%s(r *wire.Registry, name uint32, version uint32) *%s {", tn, tn)
	g.p("\to := &%s{}", tn)
	g.p("\tr.Bind(name, %sName, version, o, &o.Proxy)", tn)
	g.p("\treturn o")
	g.p("}")

	// Requests.
	for op, req := range iface.Requests {
		g.emitRequest(iface, tn, op, req)
	}

	// Event dispatch.
	g.emitDispatch(iface, tn)
}

func (g *generator) emitEnum(tn string, e Enum) {
	en := tn + camel(e.Name)
	g.p("")
	if e.Bitfield {
		g.p("// %s is the %s bitfield enum.", en, e.Name)
	} else {
		g.p("// %s is the %s enum.", en, e.Name)
	}
	g.p("type %s uint32", en)
	g.p("const (")
	for _, entry := range e.Entries {
		name := en + camel(entry.Name)
		// Entry names that start with a digit get an underscore separator
		// to stay valid identifiers (e.g. transform "90" -> Transform90 is
		// fine, but be safe for names like "1").
		if entry.Name != "" && entry.Name[0] >= '0' && entry.Name[0] <= '9' {
			name = en + "_" + entry.Name
		}
		if entry.Summary != "" {
			g.p("\t// %s: %s", name, sanitizeComment(entry.Summary))
		}
		g.p("\t%s %s = %s", name, en, entry.Value)
	}
	g.p(")")
}

// enumType resolves an enum reference like "edges" or
// "river_output_v1.presentation_mode" to a generated Go type name, or ""
// if it cannot be resolved within this package.
func (g *generator) enumType(iface Interface, ref string) string {
	if ref == "" {
		return ""
	}
	if i := strings.IndexByte(ref, '.'); i >= 0 {
		owner, name := ref[:i], ref[i+1:]
		if !g.known[owner] {
			return ""
		}
		return goName(owner) + camel(name)
	}
	return goName(iface.Name) + camel(ref)
}

// argGoType returns the Go type for an argument in a signature.
func (g *generator) argGoType(iface Interface, a Arg) string {
	switch a.Type {
	case "int":
		if t := g.enumType(iface, a.Enum); t != "" {
			return t
		}
		return "int32"
	case "uint":
		if t := g.enumType(iface, a.Enum); t != "" {
			return t
		}
		return "uint32"
	case "fixed":
		return "wire.Fixed"
	case "string":
		return "string"
	case "array":
		return "[]byte"
	case "fd":
		return "int"
	case "object", "new_id":
		if a.Interface != "" && g.known[a.Interface] {
			return "*" + goName(a.Interface)
		}
		if t, ok := wireTypes[a.Interface]; ok {
			return "*" + t
		}
		return "wire.Object"
	}
	panic("unknown arg type " + a.Type)
}

func (g *generator) eventParams(iface Interface, ev Message) string {
	var params []string
	for _, a := range ev.Args {
		params = append(params, lowerCamel(a.Name)+" "+g.argGoType(iface, a))
	}
	return strings.Join(params, ", ")
}

func (g *generator) emitRequest(iface Interface, tn string, opcode int, req Message) {
	mn := camel(req.Name)

	// A request with a new_id argument constructs and returns the new
	// object. The protocol allows at most one new_id per request.
	var newID *Arg
	var params []string
	for i := range req.Args {
		a := req.Args[i]
		if a.Type == "new_id" {
			newID = &req.Args[i]
			continue
		}
		params = append(params, lowerCamel(a.Name)+" "+g.argGoType(iface, a))
	}
	ret := ""
	newIDType := ""
	if newID != nil {
		switch {
		case g.known[newID.Interface]:
			newIDType = goName(newID.Interface)
		case wireTypes[newID.Interface] != "":
			newIDType = wireTypes[newID.Interface]
		default:
			// Untyped new_id requests (wl_registry.bind style) outside the
			// hand-written registry are not supported; none exist in the
			// protocols weir uses.
			panic(fmt.Sprintf("%s.%s: unsupported untyped or foreign new_id", iface.Name, req.Name))
		}
		ret = " *" + newIDType
	}

	g.p("")
	if req.Description.Summary != "" {
		g.p("// %s: %s", mn, sanitizeComment(req.Description.Summary))
	} else {
		g.p("// %s sends the %s request.", mn, req.Name)
	}
	if req.Type == "destructor" {
		g.p("// %s is a destructor: the object is unusable after this call.", mn)
	}
	g.p("func (o *%s) %s(%s)%s {", tn, mn, strings.Join(params, ", "), ret)
	if newID != nil {
		g.p("\tnew_ := &%s{}", newIDType)
		g.p("\to.Conn().RegisterClient(new_, &new_.Proxy)")
	}
	g.p("\te := &wire.Encoder{}")
	for _, a := range req.Args {
		v := lowerCamel(a.Name)
		switch a.Type {
		case "int":
			if g.enumType(iface, a.Enum) != "" {
				g.p("\te.PutInt(int32(%s))", v)
			} else {
				g.p("\te.PutInt(%s)", v)
			}
		case "uint":
			if g.enumType(iface, a.Enum) != "" {
				g.p("\te.PutUint(uint32(%s))", v)
			} else {
				g.p("\te.PutUint(%s)", v)
			}
		case "fixed":
			g.p("\te.PutFixed(%s)", v)
		case "string":
			g.p("\te.PutString(%s)", v)
		case "array":
			g.p("\te.PutArray(%s)", v)
		case "fd":
			g.p("\te.PutFd(%s)", v)
		case "object":
			g.p("\te.PutObject(wire.ObjectID(%s))", v)
		case "new_id":
			g.p("\te.PutUint(new_.ID())")
		}
	}
	g.p("\to.Conn().SendRequest(o, %d, e)", opcode)
	if req.Type == "destructor" {
		g.p("\to.Conn().Unregister(o)")
	}
	if newID != nil {
		g.p("\treturn new_")
	}
	g.p("}")
}

func (g *generator) emitDispatch(iface Interface, tn string) {
	g.p("")
	g.p("// Dispatch decodes and dispatches an incoming event.")
	g.p("func (o *%s) Dispatch(opcode uint16, d *wire.Decoder) error {", tn)
	g.p("\tswitch opcode {")
	for op, ev := range iface.Events {
		g.p("\tcase %d: // %s", op, ev.Name)
		var callArgs []string
		for i, a := range ev.Args {
			v := fmt.Sprintf("a%d", i)
			callArgs = append(callArgs, v)
			switch a.Type {
			case "int":
				if t := g.enumType(iface, a.Enum); t != "" {
					g.p("\t\t%sRaw, err := d.Int()", v)
					g.p("\t\tif err != nil { return err }")
					g.p("\t\t%s := %s(%sRaw)", v, t, v)
				} else {
					g.p("\t\t%s, err := d.Int()", v)
					g.p("\t\tif err != nil { return err }")
				}
			case "uint":
				if t := g.enumType(iface, a.Enum); t != "" {
					g.p("\t\t%sRaw, err := d.Uint()", v)
					g.p("\t\tif err != nil { return err }")
					g.p("\t\t%s := %s(%sRaw)", v, t, v)
				} else {
					g.p("\t\t%s, err := d.Uint()", v)
					g.p("\t\tif err != nil { return err }")
				}
			case "fixed":
				g.p("\t\t%s, err := d.Fixed()", v)
				g.p("\t\tif err != nil { return err }")
			case "string":
				g.p("\t\t%s, _, err := d.String()", v)
				g.p("\t\tif err != nil { return err }")
			case "array":
				g.p("\t\t%sRaw, err := d.Array()", v)
				g.p("\t\tif err != nil { return err }")
				g.p("\t\t%s := append([]byte(nil), %sRaw...)", v, v)
			case "fd":
				g.p("\t\t%s, err := d.Fd()", v)
				g.p("\t\tif err != nil { return err }")
			case "object":
				g.p("\t\t%sID, err := d.Object()", v)
				g.p("\t\tif err != nil { return err }")
				if a.Interface != "" && g.known[a.Interface] {
					g.p("\t\t%s, _ := o.Conn().Lookup(%sID).(*%s)", v, v, goName(a.Interface))
				} else {
					g.p("\t\t%s := o.Conn().Lookup(%sID)", v, v)
				}
			case "new_id":
				if a.Interface == "" || !g.known[a.Interface] {
					panic(fmt.Sprintf("%s.%s: unsupported untyped or foreign new_id in event", iface.Name, ev.Name))
				}
				g.p("\t\t%sID, err := d.Object()", v)
				g.p("\t\tif err != nil { return err }")
				g.p("\t\t%s := &%s{}", v, goName(a.Interface))
				g.p("\t\tif err := o.Conn().RegisterServer(%s, &%s.Proxy, %sID); err != nil { return err }", v, v, v)
			}
		}
		g.p("\t\tif o.On%s != nil {", camel(ev.Name))
		g.p("\t\t\to.On%s(%s)", camel(ev.Name), strings.Join(callArgs, ", "))
		g.p("\t\t}")
		// Avoid "declared and not used" when the handler is nil and there
		// are args: the call above references them only inside the if.
		for i, a := range ev.Args {
			_ = a
			g.p("\t\t_ = a%d", i)
		}
		g.p("\t\treturn nil")
	}
	g.p("\tdefault:")
	g.p("\t\treturn fmt.Errorf(\"unknown opcode %%d\", opcode)")
	g.p("\t}")
	g.p("}")
}

func sanitizeComment(s string) string {
	return strings.ReplaceAll(strings.TrimSpace(s), "\n", " ")
}
